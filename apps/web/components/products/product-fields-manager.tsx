type CustomFieldType = "text" | "number" | "boolean" | "select" | "date" | "json";

type ProductField = {
  id: string;
  organizationId: string;
  key: string;
  label: string;
  type: CustomFieldType;
  required: boolean;
  options: string[];
  defaultValue?: unknown;
  order: number;
  isActive: boolean;
  createdAt: string;
  updatedAt: string;
};

type FieldFormState = {
  label: string;
  type: CustomFieldType;
  required: boolean;
  optionsText: string;
  defaultValue: string;
  order: number;
  isActive: boolean;
};

type DefaultProductField = {
  key: string;
  label: string;
  type: string;
  required: boolean;
  description: string;
};

type AttributeTemplateField = {
  label: string;
  type: CustomFieldType;
  required?: boolean;
  options?: string[];
  defaultValue?: unknown;
};

type AttributeTemplate = {
  id: string;
  icon: any;
  title: string;
  description: string;
  fields: AttributeTemplateField[];
};

